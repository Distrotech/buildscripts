  if [ "${1}" == "${ARCH}" ];then
    return;
  fi;

  if [ -e /proc/sys/fs/binfmt_misc/${1} ];then
    echo -1 > /proc/sys/fs/binfmt_misc/${1}
  fi;

  case ${1} in
    arm)BFMT_STATUS=":${1}:M::\x7fELF\x01\x01\x01\x00\x00\x00\x00\x00\x00\x00\x00\x00\x02\x00\x28\x00:\xff\xff\xff\xff\xff\xff\xff\x00\xff\xff\xff\xff\xff\xff\xff\xff\xfe\xff\xff\xff:/usr/bin/qemu-arm:";;
